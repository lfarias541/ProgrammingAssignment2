##makeCacheMatrix is a function that crates a matrix that caches their own inverse by setting a value of a matrix, getting the value f the matrix, seting de invers of the matrix and gettin de matrix inverse.
makeCacheMatrix <- function(x = matrix()) {
        inv <- NULL
        set <- function(y) {
                x <<- y
                inv <<- NULL
                }
        get <- function() x
        setinverse <- function(inverse) inv <<- inverse 
        getinverse <- function () inv
        list(set = set, get = get, setinverse = setinverse, getinverse = getinverse)
}


